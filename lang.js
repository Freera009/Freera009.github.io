// ─── Language metadata ───────────────────────────────────────────────────────
var LANG_META = {
  en: { label: 'English',   flag: '🇬🇧' },
  de: { label: 'Deutsch',   flag: '🇩🇪' },
  es: { label: 'Español',   flag: '🇪🇸' },
  pt: { label: 'Português', flag: '🇵🇹' },
  tr: { label: 'Türkçe',    flag: '🇹🇷' },
  ru: { label: 'Русский',   flag: '🇷🇺' },
};

// ─── Apply all translations to the page ─────────────────────────────────────
window.applyLang = function(lang) {
  var t = window.FREERA_LANG[lang] || window.FREERA_LANG.en;

  // Translate elements with data-i18n
  document.querySelectorAll('[data-i18n]').forEach(function(el) {
    var key = el.getAttribute('data-i18n');
    if (t[key] !== undefined) el.innerHTML = t[key];
  });

  // Translate placeholder text
  document.querySelectorAll('[data-i18n-placeholder]').forEach(function(el) {
    var key = el.getAttribute('data-i18n-placeholder');
    if (t[key] !== undefined) el.placeholder = t[key];
  });

  // Translate select <option> elements
  document.querySelectorAll('[data-i18n-opt]').forEach(function(el) {
    var key = el.getAttribute('data-i18n-opt');
    if (t[key] !== undefined) el.textContent = t[key];
  });

  // Update html lang attribute
  document.documentElement.lang = lang;

  // Persist choice
  try { localStorage.setItem('freera_lang', lang); } catch(e) {}

  // Update dropdown trigger label
  _updateTrigger(lang);

  // Close dropdown
  document.querySelectorAll('.lang-dropdown').forEach(function(dd) {
    dd.classList.remove('open');
  });

  // Mark selected option
  document.querySelectorAll('.lang-option').forEach(function(o) {
    o.classList.toggle('selected', o.getAttribute('data-lang') === lang);
  });
};

// ─── Build dropdown (replaces .lang-switcher contents) ───────────────────────
function _buildDropdown() {
  document.querySelectorAll('.lang-switcher').forEach(function(switcher) {
    var current = _getSaved();

    switcher.innerHTML = '';
    switcher.style.position = 'relative';
    switcher.style.display = 'inline-block';

    // Trigger button
    var btn = document.createElement('button');
    btn.className = 'lang-trigger';
    btn.setAttribute('aria-haspopup', 'listbox');
    btn.setAttribute('aria-expanded', 'false');
    _setTriggerContent(btn, current);

    btn.addEventListener('click', function(e) {
      e.stopPropagation();
      var list = switcher.querySelector('.lang-dropdown');
      var open = list.classList.toggle('open');
      btn.setAttribute('aria-expanded', open ? 'true' : 'false');
    });

    // Dropdown list
    var list = document.createElement('ul');
    list.className = 'lang-dropdown';
    list.setAttribute('role', 'listbox');

    Object.keys(LANG_META).forEach(function(code) {
      var m = LANG_META[code];
      var li = document.createElement('li');
      li.className = 'lang-option' + (code === current ? ' selected' : '');
      li.setAttribute('role', 'option');
      li.setAttribute('data-lang', code);
      li.innerHTML = '<span class="lang-flag">' + m.flag + '</span><span class="lang-name">' + m.label + '</span>';
      li.addEventListener('click', function(e) {
        e.stopPropagation();
        window.applyLang(code);
      });
      list.appendChild(li);
    });

    switcher.appendChild(btn);
    switcher.appendChild(list);
  });

  // Close on outside click
  document.addEventListener('click', function() {
    document.querySelectorAll('.lang-dropdown').forEach(function(dd) {
      dd.classList.remove('open');
    });
    document.querySelectorAll('.lang-trigger').forEach(function(b) {
      b.setAttribute('aria-expanded', 'false');
    });
  });
}

function _setTriggerContent(btn, lang) {
  var m = LANG_META[lang] || LANG_META.en;
  btn.innerHTML =
    '<span class="lang-flag">' + m.flag + '</span>' +
    '<span class="lang-code">' + lang.toUpperCase() + '</span>' +
    '<svg class="lang-chevron" width="10" height="6" viewBox="0 0 10 6" fill="none" aria-hidden="true"><path d="M1 1l4 4 4-4" stroke="currentColor" stroke-width="1.5" stroke-linecap="round" stroke-linejoin="round"/></svg>';
}

function _updateTrigger(lang) {
  document.querySelectorAll('.lang-trigger').forEach(function(btn) {
    _setTriggerContent(btn, lang);
  });
}

function _getSaved() {
  try { return localStorage.getItem('freera_lang') || 'en'; } catch(e) { return 'en'; }
}

